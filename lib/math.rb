def addition(num1, num2)
  sum = num1 + num2
  puts sum
  sum
end

def subtraction(num1, num2)
  result = num1 - num2
  puts result
  result
end

def division(num1, num2)
  quotient = num1 / num2
  puts quotient
  quotient
end

def multiplication(num1, num2)
  product = num1 * num2
  puts product
  product
end

def modulo(num1, num2)
  remainder = num1 % num2
  puts remainder
  remainder
end

def square_root(num)
  root = Math.sqrt(num)
  puts root
  root
end